CREATE TABLE prestamo (
    num INT NOT NULL,
    id_lib INT NOT NULL,
    PRIMARY KEY (num, id_lib),
    FOREIGN KEY (num) REFERENCES amigo(num),
    FOREIGN KEY (id_lib) REFERENCES libro(id_lib)
