-- ============================================================================
-- BASE DE DATOS: CICLISMO
-- ============================================================================
DROP DATABASE IF EXISTS ciclismo_db;
CREATE DATABASE ciclismo_db;
USE ciclismo_db;

CREATE TABLE equipo (
    nomeq VARCHAR(60) PRIMARY KEY,
    director VARCHAR(80) NOT NULL
);

CREATE TABLE ciclista (
    dorsal INT PRIMARY KEY,
    nombre VARCHAR(80) NOT NULL,
    edad INT NOT NULL CHECK (edad BETWEEN 18 AND 45),
    nomeq VARCHAR(60) NOT NULL,
    FOREIGN KEY (nomeq) REFERENCES equipo(nomeq)
);

CREATE TABLE etapa (
    netapa INT PRIMARY KEY,
    km INT NOT NULL,
    salida VARCHAR(80) NOT NULL,
    llegada VARCHAR(80) NOT NULL,
    dorsal INT NOT NULL,
    FOREIGN KEY (dorsal) REFERENCES ciclista(dorsal)
);

CREATE TABLE puerto (
    nompuerto VARCHAR(80),
    altura INT NOT NULL,
    pendiente DECIMAL(4,1) NOT NULL,
    categoria INT NOT NULL,
    netapa INT NOT NULL,
    dorsal INT NOT NULL,
    PRIMARY KEY (nompuerto, netapa),
    FOREIGN KEY (netapa) REFERENCES etapa(netapa),
    FOREIGN KEY (dorsal) REFERENCES ciclista(dorsal)
);

CREATE TABLE maillot (
    codigo CHAR(3) PRIMARY KEY,
    tipo VARCHAR(60) NOT NULL,
    premio INT NOT NULL,
    color VARCHAR(30) NOT NULL
);

CREATE TABLE llevar (
    codigo CHAR(3),
    netapa INT,
    dorsal INT,
    PRIMARY KEY (codigo, netapa),
    FOREIGN KEY (codigo) REFERENCES maillot(codigo),
    FOREIGN KEY (netapa) REFERENCES etapa(netapa),
    FOREIGN KEY (dorsal) REFERENCES ciclista(dorsal)
);

