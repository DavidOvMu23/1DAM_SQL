CREATE TABLE grupo (
    cod CHAR(4) PRIMARY KEY,
    nombre VARCHAR(80) NOT NULL,
    fecha DATE NOT NULL,
    pais VARCHAR(40) NOT NULL
);

CREATE TABLE disco (
    cod CHAR(5) PRIMARY KEY,
    nombre VARCHAR(120) NOT NULL,
    fecha DATE NOT NULL,
    cod_comp CHAR(4) NOT NULL,
    cod_gru CHAR(4) NOT NULL,
