    dni CHAR(9) PRIMARY KEY,
    nombre VARCHAR(80) NOT NULL
);

CREATE TABLE club (
    cod CHAR(4) PRIMARY KEY,
    nombre VARCHAR(80) NOT NULL,
    sede VARCHAR(80) NOT NULL,
    num INT NOT NULL,
    cod_gru CHAR(4) NOT NULL,
    FOREIGN KEY (cod_gru) REFERENCES grupo(cod)
);

CREATE TABLE cancion (
    cod CHAR(5) PRIMARY KEY,
    titulo VARCHAR(120) NOT NULL,
    duracion INT NOT NULL
);

CREATE TABLE esta (
    cod CHAR(5) NOT NULL,
    can CHAR(5) NOT NULL,
    PRIMARY KEY (cod, can),
    FOREIGN KEY (cod) REFERENCES disco(cod),
    FOREIGN KEY (can) REFERENCES cancion(cod)
);

CREATE TABLE pertenece (
    dni CHAR(9) NOT NULL,
    cod CHAR(4) NOT NULL,
    funcion VARCHAR(40) NOT NULL,
    PRIMARY KEY (dni, cod),
    FOREIGN KEY (dni) REFERENCES artista(dni),
